# include <stdio.h>

void main()

{
	int A,B;
	
	printf("Enter the value of A:");
	scanf("%d",&A);
	
	printf("Enter the value of B:");
	scanf("%d",&B);
	
	printf("A + B = %d\n",A+B);
	printf("A - B = %d\n",A-B);
	printf("A / B = %d\n",A/B);
	printf("A * B = %d\n",A*B);
	printf("A % B = %d\n",A%B);
}
