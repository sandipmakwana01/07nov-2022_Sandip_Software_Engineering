    cout<<"\n"<<endl;

    // computer choice part
    cout<<" Computer Choice is : "<<number<<"\n"<<endl;
    cout<<""<<endl;

    // BASIC RULE OF ROCK , PAPER , SCISSOR GAME LIKE ROCK IS STRONGER THAN SCISSOR BUT WEAKER THAN PAPER ETC...
    if(choice == number){
        cout<<" \n DRAW\n\n"<<endl;
    }
    
    if (choice == 1 && number == 3){
        if(choice < number){
            player++;
        }
    }

    else if(choice == 1 && number == 2){
        if(choice < number){
            computer++;
        }
    }

    else if(choice == 2 && number == 3){
        if(number > choice){
            computer++;
        }
    }
    
    else if(choice == 2 && number == 1){
        if(choice > number){
            player++;
            
        }
    }
    else if(choice == 3 && number == 1){
        if(choice > number){
            computer++;
        }
    }
    else if(choice == 3 && number == 2){
        if(number < choice){
            player++;

        }
    }
    // PRESSING ANY KEY FEATURE 
